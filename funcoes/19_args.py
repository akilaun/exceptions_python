def imprimir_bairros(*bairros):
    for bairro in bairros:
        print(f"Bairro: {bairro}")

def imprimir_numeros(*number):
    for numero in number:
        print(f"Numero: {numero}")



imprimir_bairros("Vila Fatima", "Pauliceia","Campstre","Mario Dedini","Novo Horizonte")
imprimir_numeros(1,2,3,15,5,7,8)

